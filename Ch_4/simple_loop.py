# This program demonstrates a simple for loop
# That uses a list of numbers

print('I will display hello 5 times')
for num in range(5):
    print('Hello')

print('I will display the numbers 1 through 5 using range function')
for num in range(1,6):
    print(num)

print('Range function with step value')
# The step value skips over tha
